package fangtianxia;

/**
 * 
 * @author YLF
 *
 */
public class House_fang {
	private String houseType; // 户型
	private String houseArea; // 建筑面积
	private String price; // 单价
	private String houseOrientation; // 朝向
	private String blockAge; // 建筑年代
	private String floor; // 楼层
	private String decorate; // 装修程度         
	private String villageName; // 小区名称
	private String address; // 位置
	private String url; // 小区图片
	
	public String getHouseType() {
		return houseType;
	}

	public void setHouseType(String houseType) {
		this.houseType = houseType;
	}

	public String getHouseArea() {
		return houseArea;
	}

	public void setHouseArea(String houseArea) {
		this.houseArea = houseArea;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getHouseOrientation() {
		return houseOrientation;
	}

	public void setHouseOrientation(String houseOrientation) {
		this.houseOrientation = houseOrientation;
	}

	public String getBlockAge() {
		return blockAge;
	}

	public void setBlockAge(String blockAge) {
		this.blockAge = blockAge;
	}

	public String getFloor() {
		return floor;
	}

	public void setFloor(String floor) {
		this.floor = floor;
	}

	public String getDecorate() {
		return decorate;
	}

	public void setDecorate(String decorate) {
		this.decorate = decorate;
	}

	public String getVillageName() {
		return villageName;
	}

	public void setVillageName(String villageName) {
		this.villageName = villageName;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

}
